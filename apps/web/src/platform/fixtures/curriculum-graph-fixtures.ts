function graphNode(
  id: string,
  kind: CurriculumGraphNode['kind'],
  title: string,
  x: number,
  y: number,
  width: number,
  height: number,
  revealAt: CurriculumGraphNode['revealAt'],
  extra: Partial<CurriculumGraphNode> = {},
): CurriculumGraphNode {
  return { id, kind, title, x, y, width, height, revealAt, ...extra };
}

function graphEdge(from: string, to: string, label: string, kind: SemanticEdge['kind']): SemanticEdge {
  return { edgeId: `${from}->${to}`, from, to, label, kind };
}
